# list
x = ["apple", "banana", "cherry"]
print(x)
# tuple
x = ("grape", "avocado", "cocoa")
print(x)
#range
x = range(6)
# dict
x = {"nome" : "Rogério", "idade" : 45}
print(x)
# set
x = {"orange", "eggs", "meat"}
print(x)
# frozenset
x = frozenset({"cadeira", "mesa", "armário"})
print(x)
# bytes
x = b"Hello"
print(x)
# bytearray
x = bytearray(5)
print(x)
# memoryview
x = memoryview(bytes(5))
print(x)
# NoneType
x = None
print(x)